# the insert function
fruits =[ "mango","pawpaw","orange" ]
print(fruits)
fruits.append("watermelon")
print(fruits)
# the insert function
fruits.insert(2,"coconut")
print((fruits))
# pop function
fruits.pop()
print(fruits)
fruits.pop(2)
print(fruits)
# del keyword
del fruits[2]
print(fruits)
# clear function
fruits.clear()
print(fruits)
